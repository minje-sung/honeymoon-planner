@function_tool
def get_destination_info(destination: str) -> str:
    """
    指定した新婚旅行先の基本情報を返します。

    Args:
        destination: 旅行先の名前（例：ハワイ、バリ島、モルディブ、パリ、イタリア、プーケット）

    Returns:
        目的地の基本旅行情報（ベストシーズン、フライト時間、おすすめホテル等）
    """
    for key, info in DESTINATION_INFO.items():
        if key in destination or destination in key:
            result = f"【{key}の新婚旅行情報】\n\n"
            for label, value in info.items():
                if isinstance(value, list):
                    result += f"■ {label}：{', '.join(value)}\n"
                else:
                    result += f"■ {label}：{value}\n"
            return result

    registered = ", ".join(DESTINATION_INFO.keys())
    return f"「{destination}」の情報は現在データベースにありません。\n登録済みの目的地：{registered}\n\nウェブ検索で最新情報をお調べします。"
